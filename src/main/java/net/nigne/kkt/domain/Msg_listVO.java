package net.nigne.kkt.domain;

public class Msg_listVO {
	
	int no;
	String msg;
	String user_email;
	String check_user;
	int chatting_room_no;
	String date;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getCheck_user() {
		return check_user;
	}
	public void setCheck_user(String check_user) {
		this.check_user = check_user;
	}
	public int getChatting_room_no() {
		return chatting_room_no;
	}
	public void setChatting_room_no(int chatting_room_no) {
		this.chatting_room_no = chatting_room_no;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
